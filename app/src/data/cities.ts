// Simple fuzzy search function
export function fuzzySearch(query: string, items: string[]): string[] {
  if (!query.trim()) return []
  
  const normalizedQuery = query.toLowerCase().trim()
  const queryWords = normalizedQuery.split(/\s+/)
  
  return items
    .map(item => {
      const normalizedItem = item.toLowerCase()
      let score = 0
      
      // Exact match gets highest score
      if (normalizedItem === normalizedQuery) {
        score = 1000
      } else if (normalizedItem.startsWith(normalizedQuery)) {
        // Starts with query
        score = 500
      } else if (normalizedItem.includes(normalizedQuery)) {
        // Contains query as substring
        score = 300
      } else {
        // Check if all query words are present
        const allWordsMatch = queryWords.every(word => normalizedItem.includes(word))
        if (allWordsMatch) {
          score = 200
        } else {
          // Partial word matches
          const matchingWords = queryWords.filter(word => normalizedItem.includes(word))
          score = matchingWords.length * 50
        }
      }
      
      return { item, score }
    })
    .filter(({ score }) => score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, 10) // Limit to top 10 results
    .map(({ item }) => item)
}
